  const BAD_TEMPLATE_KEY_RE = /\$\{\(\(\)\s*=>/i;

  const BAD_TRANSLATION_RE = /MYMEMORY WARNING|USED ALL AVAILABLE FREE TRANSLATIONS|mymemory\.translated\.net|NEXT AVAILABLE IN/i;
  function isBadAutoTranslation(str) {
    if (!str || typeof str !== 'string') return false;
    return BAD_TRANSLATION_RE.test(str);
  }
  function sanitizeDict() {
    try {
      ['en','fr','de'].forEach(l => {
        if (!DICT[l]) return;
        Object.keys(DICT[l]).forEach(k => {
          const v = DICT[l][k];
          if (isBadAutoTranslation(v)) delete DICT[l][k];
          if (BAD_TEMPLATE_KEY_RE.test(k) || (typeof v === 'string' && BAD_TEMPLATE_KEY_RE.test(v))) delete DICT[l][k];
        });
      });
    } catch (_) {}
  }

  function normalizeLang(lang) {
    if (!lang) return 'pt';
    const l = String(lang).toLowerCase();
    if (SUPPORTED.includes(l)) return l;
    if (l.startsWith('pt')) return 'pt';
    if (l.startsWith('en')) return 'en';
    if (l.startsWith('fr')) return 'fr';
    if (l.startsWith('de')) return 'de';
    return 'pt';
  }

  function getLang() {
    try {
      const saved = localStorage.getItem(LANG_STORAGE_KEY);
      if (saved) return normalizeLang(saved);
    } catch (_) {}
    return 'pt';
  }

  function setLang(lang) {
    const l = normalizeLang(lang);
    try { localStorage.setItem(LANG_STORAGE_KEY, l); } catch (_) {}
    applyI18n(l);
    updateSwitcherUI(l);
  }

  function t(ptText, lang) {
    const l = normalizeLang(lang || getLang());
    if (l === 'pt') return ptText;
    const out = (DICT[l] && DICT[l][ptText]) ? DICT[l][ptText] : ptText;
    return isBadAutoTranslation(out) ? ptText : out;
  }

  function annotateElementsWithKeys() {
    const candidates = document.querySelectorAll(
      'a, button, label, h1, h2, h3, h4, p, span, small, strong, li, option, th, td'
    );
    candidates.forEach(el => {
      if (el.hasAttribute('data-i18n')) return;
      const raw = (el.textContent || '').replace(/\s+/g, ' ').trim();
      if (!raw) return;
      if (/^[0-9\s\-+.,€$%]+$/.test(raw)) return;
      if (/^v\d+(\.\d+)*$/i.test(raw)) return;
      if (raw === 'ALGARVE TUNA CHARTER' || raw === 'Algarve Tuna Charter') return;
      if (raw.length > 1500) return;
      el.setAttribute('data-i18n', raw);
    });

    document.querySelectorAll('[placeholder]').forEach(el => {
      if (el.hasAttribute('data-i18n-placeholder')) return;
      const ph = (el.getAttribute('placeholder') || '').trim();
      if (!ph || ph.length > 300) return;
      el.setAttribute('data-i18n-placeholder', ph);
    });

    document.querySelectorAll('[title]').forEach(el => {
      if (el.hasAttribute('data-i18n-title')) return;
      const ti = (el.getAttribute('title') || '').trim();
      if (!ti || ti.length > 300) return;
      el.setAttribute('data-i18n-title', ti);
    });
  }

  function applyTranslations(lang) {
    const l = normalizeLang(lang);
    if (l === 'pt') return;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translated = t(key, l);

      const hasChildElements = el.children && el.children.length > 0;
      if (!hasChildElements) {
        el.textContent = translated;
        return;
      }

      let replaced = false;

      // If element starts with a <strong> label (e.g., 'Cliente:'), avoid duplicating the label
      // when translating: translate full key but apply only the remainder to the text node.
      let translatedRemainder = translated;
      try {
        const firstEl = el.firstElementChild;
        if (firstEl && firstEl.tagName === 'STRONG') {
          const prefix = (firstEl.textContent || '').replace(/\s+/g, ' ').trim();
          if (prefix && translatedRemainder.startsWith(prefix)) {
            translatedRemainder = translatedRemainder.slice(prefix.length).trimStart();
          }
        }
      } catch (_) {}

      el.childNodes.forEach(n => {
        if (replaced) return;
        if (n.nodeType === Node.TEXT_NODE) {
          const raw = n.nodeValue || '';
          const trimmed = raw.replace(/\s+/g, ' ').trim();
          if (!trimmed) return;
          n.nodeValue = raw.replace(trimmed, translatedRemainder);
          replaced = true;
        }
      });

      if (!replaced) {
        el.setAttribute('aria-label', translated);
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.setAttribute('placeholder', t(key, l));
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      el.setAttribute('title', t(key, l));
    });
  }

